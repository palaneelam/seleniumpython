# Write a Python program to check if a number is prime.

# *************************************************************************
# Method-1

num = 4
for i in range(2, num+1):
    if (num % 1 == 0) and (num % num == 0) and (num % i != 0):
        result = "NOT a PRIME"
    else:
        result = "PRIME"

print(f"Given number {num} is {result} number")



