
num1 = float(input("Please enter First Number: "))
num2 = float(input("Please enter Second Number: "))

def add(num1, num2):
    addition = num1 + num2
    print("Sum of 2 given number is: ", addition)

def sub(num1, num2):
    subtract = num1 - num2
    print("Subtraction of 2 given number is: ", subtract)

def mul(num1, num2):
    prod = num1 * num2
    print("Production of 2 given number is: ", prod)

def div(num1, num2):
    division = num1 / num2
    print("Production of 2 given number is: ", division)

add(num1, num2)
